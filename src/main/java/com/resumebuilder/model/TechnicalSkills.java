package com.resumebuilder.model;

public class TechnicalSkills {
    private String programmingLanguages = "";
    private String frameworksLibraries = "";
    private String databases = "";
    private String cloudDevOps = "";
    private String tools = "";
    private String concepts = "";

    public TechnicalSkills() {}

    public String getProgrammingLanguages() { return programmingLanguages; }
    public void setProgrammingLanguages(String v) { this.programmingLanguages = v; }
    public String getFrameworksLibraries() { return frameworksLibraries; }
    public void setFrameworksLibraries(String v) { this.frameworksLibraries = v; }
    public String getDatabases() { return databases; }
    public void setDatabases(String v) { this.databases = v; }
    public String getCloudDevOps() { return cloudDevOps; }
    public void setCloudDevOps(String v) { this.cloudDevOps = v; }
    public String getTools() { return tools; }
    public void setTools(String v) { this.tools = v; }
    public String getConcepts() { return concepts; }
    public void setConcepts(String v) { this.concepts = v; }
}
